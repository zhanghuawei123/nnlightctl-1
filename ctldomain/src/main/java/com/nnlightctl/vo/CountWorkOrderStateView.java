package com.nnlightctl.vo;

public class CountWorkOrderStateView {

    private Integer total;

    private Integer finishTotal;

    private Integer noFinishTotal;

    public Integer getTotal() {
        return total;
    }

    public void setTotal(Integer total) {
        this.total = total;
    }

    public Integer getFinishTotal() {
        return finishTotal;
    }

    public void setFinishTotal(Integer finishTotal) {
        this.finishTotal = finishTotal;
    }

    public Integer getNoFinishTotal() {
        return noFinishTotal;
    }

    public void setNoFinishTotal(Integer noFinishTotal) {
        this.noFinishTotal = noFinishTotal;
    }
}
